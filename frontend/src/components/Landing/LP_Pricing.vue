            ? 'http://127.0.0.1:8000'
            : '/api')
).replace(/\/$/, '')
const accessTokenKey = 'access_token'

const isCheckoutLoading = ref(false)
const billingError = ref('')

const requireToken = () => {
    const token = localStorage.getItem(accessTokenKey)
    if (!token) {
        throw new Error('Connectez-vous pour acc√©der √† la facturation.')
